    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        try {
           stmt = conn.createStatement();

            int adminId = Integer.parseInt(id.getText());

            String sql = "SELECT * FROM ADMIN WHERE id = '" + adminId + "' ";
            rs = stmt.executeQuery(sql);
            if (rs.next()) {
                name.setText(rs.getString("name"));
                department.setText(rs.getString("department"));
                hometown.setText(rs.getString("hometown"));
                phoneNumber.setText(rs.getString("phoneNumber"));
                email.setText(rs.getString("email"));
            } else {
                JOptionPane.showMessageDialog(null, "Record Not Found");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

