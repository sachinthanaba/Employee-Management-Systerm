    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            stmt = conn.createStatement();
            int adminId = Integer.parseInt(id.getText());
            String adminName = name.getText();
            String adminDepartment = department.getText();
            String adminHometown = hometown.getText();
            String adminPhone = phoneNumber.getText();
            String adminMail = email.getText();
            
         

            String sql = "UPDATE admin SET id = '" + adminId + "', name = '" + adminName + "', department = '" + adminDepartment + "', hometown = '" + adminHometown + "', phoneNumber = '" + adminPhone + "', email = '" + adminMail + "' WHERE id = '" + adminId + "'";
            Pattern p = Pattern.compile("^[0][0-9]{9}$");
            Matcher m = p.matcher(phoneNumber.getText());

            // phone number validation
            if (m.matches()) {

                // email validation
                Pattern a = Pattern.compile("[a-zA-Z0-9][a-zA-Z0-9._]*@[a-zA-Z0-9]+([.][a-zA-Z]+)+");
                Matcher n = a.matcher(email.getText());
                if (n.matches()) {
                   stmt.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data hass been successsfully inserted");
                } else {
                    JOptionPane.showMessageDialog(null, "Invalid E-mail address");

                }

            } else {
                JOptionPane.showMessageDialog(null, "Invalid mobile number");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

