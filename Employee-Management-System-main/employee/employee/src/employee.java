    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            
            int empId = Integer.parseInt(id.getText());
            
            String sql = "SELECT * FROM EMPLOYEES WHERE empId = '"+empId+"' ";
            
            rs = stmt.executeQuery(sql);
            
            if(rs.next()){
                name.setText(rs.getString("empName"));
                department.setText(rs.getString("empDepartment"));
                hometown.setText(rs.getString("empHometown"));
                phoneNumber.setText(rs.getString("empPhone"));
                email.setText(rs.getString("empMail"));
            
            }else {
                JOptionPane.showMessageDialog(null,"Record Not Found");
            }
            
        }catch(Exception e){JOptionPane. showMessageDialog(null, e);}
    }//GEN-LAST:event_jButton3ActionPerformed

